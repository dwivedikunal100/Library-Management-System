    <p class="text " style="color:white" >Library Management System  is an enterprise resource planning system for a library, used to track books stored in library ,students registered to online library system and students who have issued books,It helps librarians to keep record of books effectively such that no information regarding book or students who have issued book is lost.
<br>
     An LMS usually comprises a relational database, software to interact with that database, and two graphical user interfaces (one for patrons, one for staff). Most ILSes separate software functions into discrete programs called modules, each of them integrated with a unified interface. Examples of modules might include:

acquisitions (ordering, receiving, and invoicing materials)
cataloging (classifying and indexing materials)
circulation (lending materials to patrons and receiving them back)
serials (tracking magazine, journals, and newspaper holdings)
the OPAC (public interface for users)
Each patron and item has a unique ID in the database that allows the ILS to track its activity.

Larger libraries use an ILS to order and acquire, receive and invoice, catalog, circulate, track and shelve materials. Smaller libraries, such as those in private homes or non-profit organizations (like churches or synagogues, for instance), often forgo the expense and maintenance required to run an ILS, and instead use a library computer system.</p>
